# Listas - Funciones Sobre Matrices [Diapo 34 - Diapo 33]

print('\n-----------------------------')
print('Funciones Sobre Matrices [Diapo 34] \n')

elem1 = [1, 2, 3]
elem2 = [4, 5, 6]
elem3 = [7, 8, 9]

list = [elem1, elem2, elem3]
print('La lista o matriz es: ', list)


print('\n-----------------------------')
print('Funciones Sobre Matrices [Diapo 34] Acceder elementos \n')

elem1 = [1, 2, 3]
elem2 = [4, 5, 6]
elem3 = [7, 8, 9]

list = [elem1, elem2, elem3]
print('El primer elemento de la lista es: ', list[0])
print('Y su segundo elemento es: ', list[0][1])



print('\n-----------------------------')
print('Funciones Sobre Matrices [Diapo 34] Acceder elementos \n')

elem1 = [1, 0, 0]
elem2 = [0, 1, 0]
elem3 = [0, 0, 1]

matriz = [elem1, elem2, elem3]
print('La matriz es: ', matriz)

elem2[1] = 7
print('La matriz es: ', matriz)

matriz[1] = elem2
print('La matriz es: ', matriz)

